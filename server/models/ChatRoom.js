const mongoose = require("mongoose");

let ChatRoom = mongoose.model("ChatRoom", {
  name: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = ChatRoom;
